    // kosongkan form
    private void kosongkan_form() {
        txtNrp.setEditable(true);
        txtNrp.requestFocus();
        txtNrp.setText(null);
        txtNama.setText(null);
        buttonGroup1.clearSelection();
        comboProdi.setSelectedItem(this);
        txtAlamat.setText(null);
    }
    
    // tampilkan data
    private void tampil_data() {
        // membuat default tabel model
        model = new DefaultTableModel();
        
        // tambah tabel
        model.addColumn("NRP");
        model.addColumn("Nama Mahasiswa");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Program Studi");
        model.addColumn("Alamat");
        
        // tampilkan isi
        try {
            PreparedStatement pre = conn.prepareStatement("SELECT * FROM mahasiswa");
            ResultSet res = pre.executeQuery();
            
            while(res.next()) {
                model.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5)
                });
            }
            
            tabelMahasiswa.setModel(model);
            
        } catch(SQLException e) {
            System.out.println("Gagal Memanggil Database : " + e.getMessage());
        }
        
    }
    
    
