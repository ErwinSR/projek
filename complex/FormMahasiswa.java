    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pre = conn.prepareStatement("INSERT INTO mahasiswa VALUES(?,?,?,?,?)");
            pre.setString(1, txtNrp.getText());
            pre.setString(2, txtNama.getText());
            
            String jk = (radioLk.isSelected()) ? "L" : "P";
            pre.setString(3, jk);
            
            String prodi = comboProdi.getSelectedItem().toString();
            pre.setString(4, prodi);
            
            pre.setString(5, txtAlamat.getText());
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            tampil_data();
            kosongkan_form();
        } catch(Exception e) {
            System.out.println("Data gagal disimpan : " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pre = conn.prepareStatement("UPDATE mahasiswa SET nama=?, jk=?, prodi=?, alamat=? WHERE nrp=?");
            
            pre.setString(1, txtNama.getText());
            
            String jk = (radioLk.isSelected()) ? "L" : "P";
            pre.setString(2, jk);
            
            String prodi = comboProdi.getSelectedItem().toString();
            pre.setString(3, prodi);
            
            pre.setString(4, txtAlamat.getText());
            
            pre.setString(5, txtNrp.getText());
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null, "Ubah data berhasil");
            tampil_data();
            kosongkan_form();
        } catch(Exception e) {
            System.out.println("Data gagal diubah : " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void tabelMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMahasiswaMouseClicked
        // TODO add your handling code here:
        txtNrp.setEditable(false);
        
        int i =tabelMahasiswa.getSelectedRow();
        txtNrp.setText(model.getValueAt(i, 0).toString());
        txtNama.setText(model.getValueAt(i, 1).toString());
        
        String pilihJk = model.getValueAt(i, 2).toString();
        if (pilihJk.equals("L")) {
            radioLk.setSelected(true);
        } else {
            radioPr.setSelected(true);
        }
        
        String pilihProdi = model.getValueAt(i, 3).toString();
        switch(pilihProdi) {
            case "Teknik Informatika":
                comboProdi.setSelectedIndex(0);
                break;
            case "Manajemen Informatika":
                comboProdi.setSelectedIndex(1);
                break;
            case "Komputer Akuntansi":
                comboProdi.setSelectedIndex(2);
                break;
            case "Sistem Informasi":
                comboProdi.setSelectedIndex(3);
                break;
        }
        
        txtAlamat.setText(model.getValueAt(i, 4).toString());
    }//GEN-LAST:event_tabelMahasiswaMouseClicked

